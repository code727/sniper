package org.sniper.kafka.support;

/**
 * MQ消息实体
 * @author  Daniele
 * @version 1.0
 */
public class Message<K, V> {
	
	/** 消息键 */
	private K key;
	
	/** 消息值 */
	private V value;
	
	public Message(K key, V value) {
		this.key = key;
		this.value = value;
	}

	public K getKey() {
		return key;
	}

	public V getValue() {
		return value;
	}


}
